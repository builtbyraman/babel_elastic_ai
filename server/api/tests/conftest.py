def _make_anthropic_mock(response_text: str, monkeypatch):
    """
    Patch the anthropic module used by ai_generator._make_client().
    The function does `import anthropic; return anthropic.Anthropic(...)`,
    so we patch the top-level `anthropic` module's `Anthropic` class.
    """
    mock_client = MagicMock()
    mock_message = MagicMock()
    mock_message.content = [MagicMock(text=response_text)]
    mock_client.messages.create.return_value = mock_message

    monkeypatch.setenv("ANTHROPIC_API_KEY", "sk-ant-test-key")

    import anthropic as anthropic_module
    monkeypatch.setattr(anthropic_module, "Anthropic", lambda **kw: mock_client)

    return mock_client


