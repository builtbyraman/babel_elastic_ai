# ── Elasticsearch mock ────────────────────────────────────────────────────────

def make_es_mock(search_hits=None, get_source=None, index_exists=False):
    """
    Build a configured MagicMock that behaves like an Elasticsearch client.
    search_hits: list of _source dicts to return from client.search()
    get_source:  _source dict to return from client.get()
    """
    mock = MagicMock()

    # Index management
    mock.indices.exists.return_value = index_exists
    mock.indices.create.return_value = {"acknowledged": True}
    mock.indices.get_mapping.return_value = {
        "logs-2024.01.01": {
            "mappings": {
                "properties": {
                    "process": {
                        "properties": {
                            "name": {"type": "keyword"},
                            "executable": {"type": "keyword"},
                            "command_line": {"type": "text"},
                        }
                    },
                    "user": {"properties": {"name": {"type": "keyword"}}},
                    "host": {"properties": {"name": {"type": "keyword"}}},
                    "@timestamp": {"type": "date"},
                    "event": {"properties": {"code": {"type": "keyword"}}},
                }
            }
        }
    }

    # Document operations
    mock.index.return_value = {"result": "created", "_id": "test-id"}

    hits = search_hits or []
    mock.search.return_value = {
        "hits": {
            "total": {"value": len(hits)},
            "hits": [{"_id": f"id-{i}", "_source": h, "_index": "logs-2024"} for i, h in enumerate(hits)],
        },
        "aggregations": {},
    }

    if get_source:
        mock.get.return_value = {"_id": "test-id", "_source": get_source}
    else:
        from elasticsearch.exceptions import NotFoundError
        mock.get.side_effect = NotFoundError(404, "not found", {})

    mock.close.return_value = None
    return mock


@pytest.fixture
def mock_es(monkeypatch):
    """
    Patches Elasticsearch in all service modules.
    Returns a factory so individual tests can configure the mock.
    """
    es_instance = make_es_mock()

    def _patch(module_path):
        monkeypatch.setattr(f"{module_path}.Elasticsearch", lambda *a, **kw: es_instance)

    for mod in [
        "services.effectiveness",
        "services.schema_drift",
        "services.rule_registry",
        "services.ai_context",
        "services.testing",
    ]:
        _patch(mod)

    return es_instance


